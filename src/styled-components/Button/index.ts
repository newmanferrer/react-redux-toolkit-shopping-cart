import styled from 'styled-components';
import { COLORS } from '../../colors';
//* ===========================================================================

//* ===========================================================================
//* 2.- Interfaces
//* ===========================================================================
interface IButtonProps {
 readonly primary?: boolean;
 readonly disabled?: boolean;
}
//* ===========================================================================

//* ===========================================================================
//* 3.- Reusable Components
//* ===========================================================================
//* ---------------------------------------------------------------------------
//* 3.1.-
//* ---------------------------------------------------------------------------
//* ---------------------------------------------------------------------------
//* ===========================================================================

//* ===========================================================================
//* 4.- Used components
//* ===========================================================================
//* ---------------------------------------------------------------------------
//* 4.1.- Button
//* ---------------------------------------------------------------------------
const Button = styled.button<IButtonProps>`
 width: 95%;
 margin: 0.25rem;
 padding: 0.35rem 0.5rem;

 color: ${({ disabled, primary }) =>
  disabled ? COLORS.error : primary ? COLORS.hoverColor : COLORS.backgroundDark};
 text-align: center;
 font-size: 0.9rem;
 font-weight: bold;
 outline: none;

 border: ${({ disabled, primary }) =>
  disabled
   ? `1px solid ${COLORS.error}`
   : primary
   ? `1px solid ${COLORS.hoverColor}`
   : `1px solid ${COLORS.backgroundDark}`};
 border-radius: 5px;

 background-color: ${({ disabled, primary }) =>
  disabled ? COLORS.hoverColor : primary ? COLORS.backgroundDark : COLORS.hoverColor};

 &:hover {
  cursor: ${({ disabled }) => (disabled ? 'not-allowed' : 'pointer')};
  color: ${({ disabled, primary }) =>
   disabled ? COLORS.error : primary ? COLORS.backgroundDark : COLORS.hoverColor};
  border: ${({ disabled, primary }) =>
   disabled
    ? `1px solid ${COLORS.error}`
    : primary
    ? `1px solid ${COLORS.backgroundDark}`
    : '1px solid transparent'};
  background-color: ${({ primary }) => (primary ? COLORS.hoverColor : COLORS.backgroundDark)};
 }
`;
//* ---------------------------------------------------------------------------
//* ===========================================================================

//* ===========================================================================
//* 5.- Exported components
//* ===========================================================================
export { Button };
